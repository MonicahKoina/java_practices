package Tracom.DataTypes.FloatDouble;

public class Float {
    //Floating point numbers are numbers that have a decimal part example 13.1
    //A double and a float are types used with floating point numbers
    //A double takes 8B while a float takes 4B
    public static void main(String[] args) {
        float f1 = 10.4F;
        double d1 = 10.4F;
        System.out.println(f1);//output 10.4
        System.out.println(d1);//output 10.399999618530273
    }
}
