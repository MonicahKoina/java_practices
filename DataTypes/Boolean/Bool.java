package Tracom.DataTypes.Boolean;

public class Bool {
    //A boolean data type is a type used with logic/boolean values
    //A boolean value represents true or false
    public static void main(String[] args) {
        boolean isAlive = true;
        if (isAlive) {
            System.out.println("Alive");
        }else {
            System.out.println("Not Alive"); //if I had initialized the boolean to be false my output would be "Not alive"
        }
    }
}
