package Tracom.Integers;

public class Short {
    //It is a type used with integers
    //all numbers in the interval [-32768, 32767](16 bits) can be stored in a short variable
    public static void main(String[] args) {
        short s1 = 2898;
        short s2 = 7898;
        System.out.println(s1+s2);//expression output 10796
    }
}
