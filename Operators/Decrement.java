package Tracom.Operators;

public class Decrement {
    //Decrement reduces the value of the variable by one
    public static void main(String[] args) {
        int x=4;
        int y=--x;
        System.out.println("The value of x is "+ x + " and the value of y is "+ y);//output The value of x is 3 and the value of y is 3
    }
}
